const initialState = {
  todos: [
    {
      id: 0,
      title: "",
      content: "",
      isDone: false,
    },
  ],
  todo: {
    id: 0,
    title: "",
    content: "",
    isDone: false,
  },
};

// ë¦¬ë“€ì„œ
const todos = (state = initialState, action) => {
  switch (action.type) {
    case "ADD_TODO":
      return {
        ...state,
        todos: [...state.todos, action.payload],
      };
    case "DELETE_TODO":
      return {
        ...state,
        todos: state.todos.filter((todo) => todo.id !== action.payload),
      };
    case "TOGGLE_TODO":
      return {
        ...state,
        todos: state.todos.map((todo) => {
          if (todo.id === action.payload) {
            return {
              ...todo,
              isDone: !todo.isDone,
            };
          } else {
            return todo;
          }
        }),
      };
    default:
      return state;
  }
};

